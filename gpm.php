<p align="justify" class="white">The Campaign itself is a part of Citizen Science and Innovation Project, undertaken by the then Biodiversity of Paschim Bardhaman, was rechristened Green Picnic Mission from 2017-2018, on account of the multipurpose goals, that were set to be achieved, via this program. The objectives now included not only the purpose of merely cleaning the picnic sites but also creating environmental awareness among the picnickers, which in turn, would yield long-term results, in other areas as well.<p>
    
<p align="justify" class="white">We feel fortunate, in having the patronage of West Bengal Pollution Control Board, Durgapur regional Office, Durgapur Forest Division (Directorate of Wildlife, Government of West Bengal), The New Horizon Institute of Technology, Durgapur (a leading private Polytechnic College in Durgapur), Durgapur Government College, and Durgapur Snakes and Wild Lovers during GPM 2017-2018. The program was also modelled in Howrah, Cooch Behar and Purulia districts of West Bengal, with some local modifications.<p>
<p align="justify" class="white">The participants of GPM 2017-2018 were awarded certificates on Annual Felicitation Program, held on 29th September 2018, at The New Horizon Institute of Technology, Durgapur.</p>
