                                    <div style="margin:8px;">

<p align="justify" class="white">The Green Picnic Mission (henceforth, GPM) is a name that, most nature lovers and enthusiasts, from Durgapur and adjacent areas, can relate to easily. The program was initiated in 2016-2017, under the name of Durgapur Barrage Cleaning Campaign, under the banner of erstwhile <i>Birding Durgapur</i>. A brainchild of our founding member as well as Governing Body member <b>Nirjhar Banerjee</b>, GPM is a culmination of a movement that started with <b>only five (5)</b> nature enthusiasts and bird lovers [Milan Kanti Mandal (DFO, Durgapur), Kamol Raha, Abhijeet Sett, Nirjhar Banerjee and Sagar Adhurya], during 2016-2017. Initially undermined, this project turned out to be a grand success, in the following year 2017-2018, when the number of participants skyrocketed to around <b>82</b>.</p>
</div>
