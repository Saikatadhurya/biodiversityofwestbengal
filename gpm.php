<h3 align="center" class="white">Green Picnic Mission 2018-2019</h3>
<p align="justify" class="white">This year, the Green Picnic Mission is going to be special for all of us. This is the first time, when GPM will be organized, as a part of Unmesh, our Citizen Science and Innovations Project, and that too, under the aegis of WINGS India, the newly reorganized and rechristened avatar of erstwhile Birding Durgapur/Biodiversity of Paschim Bardhaman Group.  GPM 2018-2019 is also going to spread wings to newer picnic sites in Durgapur and Asansol, under the direct guidance of WINGS India, and in Murshidabad district with collaboration with Wildlife Conservation Murshidabad, our strategic partner.<p>

<p align="justify" class="white">Green Picnic Mission 2018-2019 has been scheduled on <b>25th December 2018, 31st December 2018 and 1st January 2019</b>- the three peak days of winter picnics in West Bengal.<p>
<p align="justify" class="white">Come and join us on these three days, and help us make our picnic sites, cleaner and greener, so that all of us, including our natural neighbours, peacefully co-exist side by side, with our needs fulfilled in more mutually satisfying manner, and that our future generations are not deprived of the natural heritage.
