<p align="justify" class="white">The need for such a campaign, arose out of the immediate need, of educating and making the picnickers aware about the ill-effects of using use-and-throw plastic and thermoplastic utensils and their improper management, uncontrolled playing of DJ microphones (or, as some others say, boomboxes),  stampeding on nests of some river-bank birds build on the sandbanks, and several other nuisances.<p>
